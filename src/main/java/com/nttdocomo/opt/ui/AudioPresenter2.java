package com.nttdocomo.opt.ui;

/**
 * Defines attribute constants for the enhanced audio presenter.
 */
public class AudioPresenter2 {
    /**
     * Attribute key for loop playback control.
     */
    public static final int LOOP = 128;
    /**
     * Attribute key for tempo control.
     */
    public static final int TEMPO = 129;
    /**
     * Attribute key for key-shift control.
     */
    public static final int KEY_SHIFT = 130;
    /**
     * Attribute key for volume control.
     */
    public static final int VOLUME = 131;
    /**
     * Attribute key for panpot control.
     */
    public static final int PANPOT = 132;
    /**
     * Attribute key for track-assignment control.
     */
    public static final int TRACK_ASSIGNMENT = 133;

    /**
     * Applications cannot create this object directly.
     */
    protected AudioPresenter2() {
    }
}
